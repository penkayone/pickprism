/**
 * Главный фильтр: подменяет переводы из ru_RU ядра WordPress.
 *
 * @param string $translated Переведённая строка.
 * @param string $original   Оригинальная (английская) строка.
 * @param string $domain     Текст-домен.
 * @return string
 */
add_filter(
	'gettext',
	static function ( $translated, $original, $domain ) {
		// Трогаем только ядро. Тема (pickprism), Rank Math, Wordfence и т.п. — мимо.
		if ( $domain !== 'default' ) {
			return $translated;
		}
		$map = pickprism_labels_map();
		if ( isset( $map[ $translated ] ) ) {
			return $map[ $translated ];
		}
		return $translated;
	},
	10,
	3
);

/**
 * Контекстные строки (_x) — иногда WP использует их для меню/панели.
 */
add_filter(
	'gettext_with_context',
	static function ( $translated, $original, $context, $domain ) {
		if ( $domain !== 'default' ) {
			return $translated;
		}
		$map = pickprism_labels_map();
		if ( isset( $map[ $translated ] ) ) {
			return $map[ $translated ];
		}
		return $translated;
	},
	10,
	4
);

/**
 * Plurals (_n) — отдельный фильтр, иначе «Рубрики записей» через ngettext
 * пройдёт мимо обычного gettext.
 */
add_filter(
	'ngettext',
	static function ( $translated, $single, $plural, $number, $domain ) {
		if ( $domain !== 'default' ) {
			return $translated;
		}
		$map = pickprism_labels_map();
		if ( isset( $map[ $translated ] ) ) {
			return $map[ $translated ];
		}
		return $translated;
	},
	10,
	5
);

/**
 * the_archive_title prefix: «Рубрика: » / «Метка: » → «Категория: » / «Тег: ».
 * Подстраховка на случай, если ядро меняет префикс через свою функцию
 * без gettext-вызова (зависит от версии WP).
 */
add_filter(
	'get_the_archive_title_prefix',
	static function ( string $prefix ): string {
		$prefix = str_replace(
			array( 'Рубрика:', 'Метка:' ),
			array( 'Категория:', 'Тег:' ),
			$prefix
		);
		return $prefix;
	}
);
